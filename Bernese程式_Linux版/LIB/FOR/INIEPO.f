      SUBROUTINE INIEPO(MAXSAS,MAXFLS,SYNFLG,ELEVA,LOCQ)
CC
CC NAME       :  INIEPO
CC
CC PURPOSE    :  INITIALIZE EPOCH PROCESSING FOR PROGRAM GPSEST
CC
CC PARAMETERS :
CC         IN :  MAXSAS : MAXIMUM NUMBER OF SAT./EPOCH        I*4
CC               MAXFLS : MAXIMUM NUMBER OF FILES             I*4
CC        OUT :  SYNFLG(K),K=1,..,MAXFLS:                     CH*1
CC                        SYNCHRONIZATION FLAG
CC               ELEVA(2,I),I=1,..,NSASES: ELEVATIONS FOR     R*8
CC                        MAX. TWO SITES
CC               LOCQ(K,I),K=1,..,MAXLCQ, I=1,..,NPAR:        I*4
CC                        PARAMETER DESCRIPTOR
CC
CC REMARKS    :  ---
CC
CC AUTHOR     :  G.BEUTLER
CC
CC VERSION    :  3.4  (JAN 93)
CC
CC CREATED    :  87/10/20 17:21
CC
CC CHANGES    :  29-JAN-97 : SS: "ELEVA" AS TWO-DIM. ARRAY,
CC                               REMOVE "MAXSAT" FROM PARAM.LIST
CC               21-JAN-98 : SS: TIME-CONSUMING INITIALIZATION OF
CC                               "ASING" AND "INDSNG" REMOVED
CC               25-OCT-00 : RD: COUNT OBS. FOR CLOCKS
CC               22-NOV-00 : TS: REMOVE LOCQ INITIALIZATION (NOT NEEDED?)
CC               17-FEB-03 : LM: USE P_GPSEST
CC               08-MAR-03 : MR: MAXAMB MOVED TO M_MAXDIM
CC               28-JUN-04 : RD: REMOVE DISPENSABLE PARAMETERS
