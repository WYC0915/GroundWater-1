CC      1987     UNIVERSITY OF BERN
CC               SWITZERLAND
CC
C*
      USE p_gpsest, ONLY: MAXLCQ
      IMPLICIT NONE
C
C DECLARATIONS INSTEAD OF IMPLICIT
C --------------------------------
      INTEGER*4 I     , K     , MAXFLS, MAXSAS
C
CCC       IMPLICIT REAL*8 (A-H,O-Z)
CCC       IMPLICIT INTEGER*4 (I-N)
C
C
      CHARACTER*1  SYNFLG(*)
C
      INTEGER*4    LOCQ(MAXLCQ,*)
C
      REAL*8       ELEVA(2,*)
C
C INITIALIZE ELEVA
      DO 15 I=1,MAXSAS
        DO 10 K=1,2
          ELEVA(K,I)=0.D0
10      CONTINUE
15    CONTINUE
C
C INITIALIZE SYNFLG
      DO 20 I=1,MAXFLS
        SYNFLG(I)='U'
20    CONTINUE
C
C INITIALIZE COUNTER FOR SAT:/STA. CLOCKS
C      DO 30 IPAR=1,MAXPAR
C        IF ((LOCQ(1,IPAR).EQ.23) .OR. (LOCQ(1,IPAR).EQ.24))
C     1     LOCQ(6,IPAR)=0
C30    CONTINUE
C
      RETURN
      END SUBROUTINE

